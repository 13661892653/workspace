package com.easyrong.data.base.days07_exercise;

public class PrintABC {
    /*
        输出26个大小写字母
        A-Z 65-90
        a-z 97-122
     */
    public static void main(String[] args) {
        for (int i = 65; i <= 90; i++) {
            System.out.print((char) i);
        }
        System.out.println();
        for (int i = 97; i <= 122; i++) {
            System.out.print((char) i);
        }

        int[] i={1,2,3,4,5};

    }
}
